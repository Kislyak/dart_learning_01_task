void main() {
  // Завдання 1: Математичні оператори
  double height = 30.9;
  double width = 18.4;
  double area = height * width;
  double perimeter = 2 * (height + width);
  int fullSquares = (height ~/ 1) * (width ~/ 1);
  print('Area: $area');
  print('Perimeter: $perimeter');
  print('Full squares: $fullSquares');
}
